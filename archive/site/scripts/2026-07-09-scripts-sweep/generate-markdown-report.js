const fs = require('fs');

try {
  let coverageData = JSON.parse(fs.readFileSync('results/coverage-reports/planner/coverage-report.json', 'utf8'));
  
  // Read fake tests - file might contain BOM
  let fakeTestsContent = fs.readFileSync('results/tests/fake-tests.json', 'utf16le').replace(/^\uFEFF/, '');
  let fakeTests = JSON.parse(fakeTestsContent);

  let md = '# Test Coverage & Quality Report\n\n';

  md += '## Global Coverage\n';
  md += `- **Statements**: ${coverageData.total.statements.pct}%\n`;
  md += `- **Branches**: ${coverageData.total.branches.pct}%\n`;
  md += `- **Functions**: ${coverageData.total.functions.pct}%\n`;
  md += `- **Lines**: ${coverageData.total.lines.pct}%\n\n`;

  md += '## Skipped Lines by File\n\n';
  let hasSkipped = false;

  for (const file of coverageData.files) {
    // Attempt to extract skipped lines. If not available in this JSON format, we might need to parse it differently.
    // Vitest (istanbul) usually outputs line by line in coverage-final.json. This coverage-report.json seems to be generated by a custom script `generate-coverage-report.mjs`.
    // Let's check what properties exist in `file`.
    const uncoveredLines = file.uncoveredLineNumbers || file.skippedLines || [];
    if (uncoveredLines.length > 0) {
      hasSkipped = true;
      md += `### ${file.relativePath}\n`;
      md += `- **Lines covered**: ${file.lines.pct}%\n`;
      md += `- **Skipped/Uncovered Lines**: ${uncoveredLines.join(', ')}\n\n`;
    }
  }

  if (!hasSkipped) {
    md += 'No skipped lines reported or data format does not support skipped lines.\n\n';
  }

  md += '## Fake Tests\n\n';
  if (fakeTests.length === 0) {
    md += 'No fake tests found! All tests contain assertions.\n';
  } else {
    for (const test of fakeTests) {
      md += `- **File**: \`${test.file.replace(/\\/g, '/').split('/site/')[1]}\`\n`;
      if (test.test) md += `  - **Test**: "${test.test}"\n`;
      md += `  - **Reason**: ${test.reason}\n`;
    }
  }

  fs.writeFileSync('results/coverage-report.md', md, 'utf8');
  console.log('Report generated at results/coverage-report.md');
} catch (e) {
  console.error('Error:', e);
}
